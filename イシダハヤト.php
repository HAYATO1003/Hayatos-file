    <span style="color:mediumblue;"><h2>　＜研究概要＞</h2></span>
    　【ガス発酵による有用物質生産の実用株の育種】<br>
    　ガス発酵は微生物を活用し、二酸化炭素からモノづくりをする革新的な技術である。<br>
    　具体的には、地球温暖化の原因物質である二酸化炭素と次世代のエネルギーを担う水素を資源とし、お酢の主成分である酢酸やバイオ燃料の原料となるエタノールなどを製造できる。<br>
    　しかし、この微生物は酸素があると生育できない。私は社会実装に向け、酸素耐性が向上した個体の取得を目指し、酸素耐性能の評価及び遺伝子組み換えを活用したメカニズムの解明を行っている。<br>
    

